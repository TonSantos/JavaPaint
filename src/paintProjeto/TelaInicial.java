    private void botaoCriarLinhaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoCriarLinhaActionPerformed
        // TODO add your handling code here:
        //System.out.println("vc clicou a porra do botão da linha"); 
        try{
        String valorx1, valory1, valorx2, valory2;
        Ponto posicao1 = new Ponto(0, 0);
        Ponto posicao2 = new Ponto(0, 0);
        valorx1 = coordXPrimLinha;
        valory1 = coordYPrimLinha;
        valorx2 = coordXSegLinha;
        valory2 = coordYSegLinha;

        posicao1.setX(Integer.parseInt(valorx1));
        posicao1.setY(Integer.parseInt(valory1));
        posicao2.setX(Integer.parseInt(valorx2));
        posicao2.setY(Integer.parseInt(valory2));
        
        
        figura.add(new Linha(posicao1, posicao2,CorGlobal));
        System.out.println("numero de figuras " + figura.size());
        ((desenhaC)this.papel).desenhoLinha(posicao1, posicao2,CorGlobal);
            if(posicao1.getX() < 0|| posicao1.getY() <0 || posicao2.getX() < 0 || posicao2.getY() < 0 ){
            throw new IllegalArgumentException();
            }
            if(posicao1.getX() > papel.getWidth()|| posicao1.getY() >papel.getHeight() || posicao2.getX() > papel.getWidth() || posicao2.getY() > papel.getHeight()){
            throw new ForaAreaException();
            }
        }
        catch(NumberFormatException e ){
           JOptionPane.showMessageDialog(null,"Digite valores numéricos");
        }
        catch(IllegalArgumentException e){
        JOptionPane.showMessageDialog(null,"Digite valores positivos ");
        }
        catch (ForaAreaException ex) {
            JOptionPane.showMessageDialog(null, "Coordenadas      fora da area de desenho! ");
        } 
    }//GEN-LAST:event_botaoCriarLinhaActionPerformed

