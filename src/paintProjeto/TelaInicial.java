    private void menuRecarregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_menuRecarregarActionPerformed
        try {
            JFileChooser seletorArquivos =new JFileChooser();
            int i;
           
            figura = null;
            figura = (ArrayList<Figura>) d.deserializar("ArquivoFiguras.txt");
            for(i=0; i< figura.size(); i++){
                //System.out.println("ArrayList: " + f.getNome() + " - " + f.getCor());
                if(figura.get(i) instanceof Circulo){
                    Circulo c = (Circulo)figura.get(i);
                ((desenhaC)this.papel).desenhoCirculo( c.getPosicao(), c.getRaio(),c.getCor());
                }
                if(figura.get(i) instanceof Retangulo){
                    Retangulo r = (Retangulo)figura.get(i);
                ((desenhaC)this.papel).desenhoRetangulo( r.getPosicao(), r.getAltura(), r.getLargura(), r.getCor());
                }
                if(figura.get(i) instanceof Linha){
                    Linha l = (Linha)figura.get(i);
                ((desenhaC)this.papel).desenhoLinha( l.getPosicao(), l.getSegPonto(),l.getCor());
                }
                
                
            }
        } catch (Exception ex) {
            System.err.println("Falha ao  deserializar! - " +
                               ex.toString());
        }
    }//GEN-LAST:event_menuRecarregarActionPerformed

